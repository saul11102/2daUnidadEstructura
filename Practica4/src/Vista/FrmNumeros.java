    private void generarNumeros() {
        int cantidadNumeros = 20000;
        for (int i = 0; i < cantidadNumeros; i++) {
            listaNumeros.insertarNodo((int) (Math.random() * 1000));
        }
    }

    private void ordenar() {
        String metodo = cbxMetodo.getSelectedItem().toString();
        Integer tipo = 0;
        
        if (cbxOrden.getSelectedItem().toString().equals("Descendente")) {
            tipo = 1;
        }
        long startTime = System.currentTimeMillis();
        if (metodo.equalsIgnoreCase("QuickSort")) {

            QuickSort ordenar = new QuickSort();
            {
                try {
                    modelo.setLista(ordenar.quickSort(listaNumeros, tipo));
                } catch (Exception ex) {
                    System.out.println(ex.getMessage());
                }
            }
        }

        if (metodo.equalsIgnoreCase("MergeSort")) {
            try {
                MergeSort ordenar = new MergeSort();
                modelo.setLista(ordenar.mergeSort(listaNumeros, tipo));
                
            } catch (Exception ex) {
                Logger.getLogger(FrmNumeros.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        long finalTime = System.currentTimeMillis() - startTime;
        System.out.println("Tiempo que tardo en ordenar: " + finalTime);
        tblNumeros.setModel(modelo);
        tblNumeros.updateUI();
       System.out.println("ordenado");
    }

